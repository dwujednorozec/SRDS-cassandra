

public class Books {
    private int id;
    private int nr_of_free_books;
    private int total_books;
    private string book_name;

    public int getId() {
        return id;
    }

    public int getNumberOfFreeBooks() {
        return nr_of_free_books;
    }

    public int getTotalBooks() {
        return total_books;
    }

    public string getBookName() {
        return book_name;
    }

}
